#!/bin/python3

from vector import Vector

v1 = Vector(2,10, 5)
v2 = Vector(5,-2, 3)
print("Sum of vectors", Vector.__add__(v1, v2))
print("Difference of vectors", Vector.__sub__(v1, v2))
print("Magnitude of vector 1", Vector.__mag__(v1))
print("Magnitude of vector 2", Vector.__mag__(v2))
print("Dot product of vectors", Vector.__dot_product__(v1, v2))
print("Cross product of vectors", Vector.__cross_product__(v1,v2))

A1 = Vector(0, 0, 0)
A2 = Vector(1, 0, 0)
A3 = Vector(0, 1, 0)
A12 = Vector.__sub__(A2, A1)
A13 = Vector.__sub__(A3, A1)
A23 = Vector.__sub__(A3, A2)
area_A = Vector.__area__(A12, A13)
print(f"Triangle area A: {area_A}")
Angle_A12_A13 = Vector.__angle__(A12, A13)
Angle_A12_A23 = Vector.__angle__(A12, A23)
Angle_A13_A23 = Vector.__angle__(A13, A23)
print(f"Angles in triangle A: ({Angle_A12_A13},{Angle_A12_A23}, {Angle_A12_A23})")

