#!/bin/python3

from vector import Vector
from vector import VectorP

v1 = VectorP(1, 1.5708, 0)
v2 = VectorP(1, 1.5708, 3.14159)
print("Sum of vectors", VectorP.__add__(v1, v2))
print("Difference of vectors", VectorP.__sub__(v1, v2))
print("Magnitude of vector 1", VectorP.__mag__(v1))
print("Magnitude of vector 2", VectorP.__mag__(v2))
print("Dot product of vectors", VectorP.__dot_product__(v1, v2))
print("Cross product of vectors", VectorP.__cross_product__(v1,v2))

A1 = Vector(0.0, 0.0, 0.0)
